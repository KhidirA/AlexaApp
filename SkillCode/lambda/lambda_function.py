class WhosAtTheDoorIntentHandler(AbstractRequestHandler):
    """Handler for Whos At The Door Intent"""
    def can_handle(self, handler_input):
        return ask_utils.is_intent_name("WhosAtTheDoorIntent")(handler_input)
    
    def handle(self, handler_input):
        
        sts_client = boto3.client('sts')
        assumed_role_object = sts_client.assume_role(RoleArn="(IAM ROLE ARN)", RoleSessionName="AssumeRoleSession")
        credentials=assumed_role_object['Credentials']
        
        s3 = boto3.resource('s3',
                    aws_access_key_id=credentials['AccessKeyId'],
                    aws_secret_access_key=credentials['SecretAccessKey'],
                    aws_session_token=credentials['SessionToken'],
                    region_name='us-east-1')
                    
        rekogClient = boto3.client('rekognition',
                    aws_access_key_id=credentials['AccessKeyId'],
                    aws_secret_access_key=credentials['SecretAccessKey'],
                    aws_session_token=credentials['SessionToken'],
                    region_name='us-east-1')
                    
        s3Client = boto3.client('s3',
                    aws_access_key_id=credentials['AccessKeyId'],
                    aws_secret_access_key=credentials['SecretAccessKey'],
                    aws_session_token=credentials['SessionToken'],
                    region_name='us-east-1')
        
        try:
            
            inputFacesBucket = s3.Bucket('famous-people-faces')
            savedFacesBucket = s3.Bucket('famous-people-faces-source')
            pic_keys_input = [o.key for o in inputFacesBucket.objects.all()]
            pic_keys_saved = [o.key for o in savedFacesBucket.objects.all()]
            inputKey = random.randint(0,len(pic_keys_input)-1) # picks a random input picture from the bucket
            inputKey = 1
            
            random_picture = pic_keys_input[inputKey]

            rekog_response = rekogClient.detect_faces(Image={'S3Object':{'Bucket':"famous-people-faces",'Name':random_picture}},Attributes=['ALL'])
            if not rekog_response['FaceDetails']:
                speak_output = "No person detected"
                return (
                    handler_input.response_builder
                        .speak(speak_output)
                        .response
                )
                
            
            speak_output = "Unknown person at the door"
            
            for pic_key in pic_keys_saved:
                response = rekogClient.compare_faces(SourceImage={'S3Object':{'Bucket':"famous-people-faces-source",'Name':pic_key}},TargetImage={'S3Object':{'Bucket':"famous-people-faces",'Name':random_picture}},SimilarityThreshold=90.0)
                
                for faceMatch in response['FaceMatches']:
                    similarity = faceMatch['Similarity']
                    if similarity > 95:
                        s3Response = s3Client.get_object_tagging(Bucket="famous-people-faces-source", Key=pic_key)
                        name = s3Response['TagSet'][0]['Value']
                        speak_output = str(name) + " is at the door."

            
        except Exception as e: 
            speak_output = str(e)
            
        return (
            handler_input.response_builder
                .speak(speak_output)
                .response
        )

